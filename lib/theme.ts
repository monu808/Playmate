// Helper function to get color value
export const getColor = (colorPath: string): string => {
  const parts = colorPath.split('.');
  let value: any = colors;
  
  for (const part of parts) {
    value = value[part];
    if (value === undefined) return colors.primary[600];
  }
  
  return value;
};

